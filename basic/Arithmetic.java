package basic;
import java.util.*;


public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        int sum = a + b;
        System.out.println(sum);

        int diff = a - b;
        System.out.println(diff);

        int multi = a * b;
        System.out.println(multi);

        int div = a / b;
        System.out.println(div);
    }
}
